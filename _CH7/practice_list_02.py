def grid_translation(gname):
    ycount = len(gname)
    xcount = len(gname[1])
    print('"Start"')
    print(f'width  : {xcount}')
    print(f'height : {ycount}')
    print('')

    for x in range(xcount):
        for y in range(ycount):
            print(gname[y][x], end = '')
        print()
    return f'\nwidth  : {ycount}\nheight : {xcount}\n"End"'

